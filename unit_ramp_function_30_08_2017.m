clc;clear all; close all;
t=-5:0.005:5;
u1=0.5*(sign(t-1)+1);
u2=0.5*(sign(2*t+1)+1);
ims=0.5*(sign(t-2.5)+1)-0.5*(sign(t-2.5-0.005)+1);
tt=0:5;
rpm=0.5*(sign(tt-3)+1);
subplot(4,1,1);
plot(t,u1);
axis([-5 5 0 1.5]);
title('UNIT STEP SIGNAL');
subplot(4,1,2); 
plot(t,u2);
axis([-5 5 0 1.5]);
title('UNIT STEP FUNCTION');
subplot(4,1,3);
plot(t,ims);
axis([-5 5 0 1.5]);
title('IMPLSE FUNCTION');
subplot(4,1,4);
plot(tt,rpm);
grid on;
xlabel('TIMES>>>>>>>');
ylabel('AMPLITUDE>>>>>>>');
title('UNIT RAMP FUNCTION');