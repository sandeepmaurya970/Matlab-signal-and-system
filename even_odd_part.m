clc; close all; clear all;
n=-10:15;
x=[zeros(1,10) ones(1,10) zeros(1,6)];
m=-fliplr(n); m1=min([m,n]);
m2=max([m,n]);
m=m1:m2; nm=n(1)-m(1);
n1=1:length(n);
x1=zeros(1,length(m));
x1(n1+nm)=x; x=x1;
xe=0.5*(x+fliplr(x));
xo=0.5*(x-fliplr(x));
figure;
t=-15:15;
subplot(3,1,1); stem(t,x);
axis([-15 15 0 1.5]);
title('original signal');
subplot(3,1,2);
stem(t,xe);
title('even part of signal x(n)');
subplot(3,1,3);
stem(t,xo);
axis([-15 15 -1 1]);
xlabel('time==>');
ylabel('amplitude');
title('odd part of signal');